#include "header.h"

void	ft_touch(char *map, char ***result, int x, int y)
{
	int		co;

	co = 0;
	while (co < 4)
	{
		(*result)[(map[co] - '0') + y][(map[co + 4] - '0') + x] = map[8];
		co++;
	}
}
